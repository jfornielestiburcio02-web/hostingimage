            <INPUT TYPE="PASSWORD" NAME="CLAVE" PLACEHOLDER="CONTRASEÑA" REQUIRED="REQUIRED" />
            <INPUT TYPE="SUBMIT" VALUE="ENTRAR" CLASS="BOTON" />
        </FORM>
        <?PHP IF ($MENSAJE_ERROR): ?>
