<?PHP
$PROJECT_ID = "hostingimage1";
$FIELD_NAME = "contrasena";
$MENSAJE_ERROR = "";

IF ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $USUARIO_INPUT = STRTOLOWER(TRIM($_POST['USUARIO'] ?? ''));
    $CLAVE_INPUT = TRIM($_POST['CLAVE'] ?? '');

    IF (!EMPTY($USUARIO_INPUT) && !EMPTY($CLAVE_INPUT)) {
        
        // CONSULTAMOS LA COLECCIÓN ENTERA POR SI EL DOCUMENTO TIENE CARACTERES INVISIBLES
        $URL = "HTTPS://FIRESTORE.GOOGLEAPIS.COM/V1/PROJECTS/$PROJECT_ID/DATABASES/(DEFAULT)/DOCUMENTS/USUARIOS";
        
        $CH = CURL_INIT($URL);
        CURL_SETOPT($CH, CURLOPT_RETURNTRANSFER, TRUE);
        CURL_SETOPT($CH, CURLOPT_TIMEOUT, 10);
        
        $RESPONSE = CURL_EXEC($CH);
        $HTTP_CODE = CURL_GETINFO($CH, CURLINFO_HTTP_CODE);
        CURL_CLOSE($CH);

        IF ($HTTP_CODE == 200) {
            $DATA = JSON_DECODE($RESPONSE, TRUE);
            $ENCONTRADO = FALSE;

            // RECORREMOS LOS DOCUMENTOS QUE DEVUELVE FIREBASE
            IF (ISSET($DATA['DOCUMENTS'])) {
                FOREACH ($DATA['DOCUMENTS'] AS $DOC) {
                    // EXTRAEMOS EL NOMBRE DEL DOCUMENTO DE LA RUTA COMPLETA
                    $FULL_PATH = $DOC['NAME'];
                    $PARTS = EXPLODE('/', $FULL_PATH);
                    $DOC_ID = END($PARTS);

                    IF (STRTOLOWER($DOC_ID) === $USUARIO_INPUT) {
                        $ENCONTRADO = TRUE;
                        $CLAVE_FIRESTORE = $DOC['FIELDS'][$FIELD_NAME]['STRINGVALUE'] ?? '';

                        IF ($CLAVE_INPUT === $CLAVE_FIRESTORE) {
                            SESSION_START();
                            $_SESSION['EMPRESA'] = $USUARIO_INPUT;
                            HEADER("LOCATION: /PANEL-CONTROL.PHP");
                            EXIT;
                        } ELSE {
                            $MENSAJE_ERROR = "CONTRASEÑA INCORRECTA PARA " . STRTOUPPER($DOC_ID);
                        }
                        BREAK;
                    }
                }
            }

            IF (!$ENCONTRADO) {
                $MENSAJE_ERROR = "EL USUARIO '" . STRTOUPPER($USUARIO_INPUT) . "' NO SE ENCUENTRA EN LA LISTA DE FIRESTORE.";
            }

        } ELSE {
            $ERROR_INFO = JSON_DECODE($RESPONSE, TRUE);
            $DETALLE = $ERROR_INFO['ERROR']['MESSAGE'] ?? "ERROR DESCONOCIDO";
            $MENSAJE_ERROR = "ERROR API $HTTP_CODE: $DETALLE";
        }
    } ELSE {
        $MENSAJE_ERROR = "RELLENA TODOS LOS CAMPOS, COJONES";
    }
}
?>
