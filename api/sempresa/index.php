<?PHP
// CONFIGURACIÓN OCULTA
$PROJECT_ID = "hostingimage1";
$FIELD_NAME = "contrasena";
$MENSAJE_ERROR = "";

IF ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // CONVERTIMOS EL USUARIO A MINÚSCULAS PARA EVITAR EL ERROR DE "NO EXISTO"
    $USUARIO_INPUT = STRTOLOWER(TRIM($_POST['USUARIO'] ?? ''));
    $CLAVE_INPUT = $_POST['CLAVE'] ?? '';

    IF (!EMPTY($USUARIO_INPUT) && !EMPTY($CLAVE_INPUT)) {
        // CONSULTA A LA API REST DE FIRESTORE (EL ID DEL DOCUMENTO ES EL USUARIO)
        $URL = "HTTPS://FIRESTORE.GOOGLEAPIS.COM/V1/PROJECTS/$PROJECT_ID/DATABASES/(DEFAULT)/DOCUMENTS/USUARIOS/" . URLENCODE($USUARIO_INPUT);
        
        $CH = CURL_INIT($URL);
        CURL_SETOPT($CH, CURLOPT_RETURNTRANSFER, TRUE);
        CURL_SETOPT($CH, CURLOPT_TIMEOUT, 10);
        CURL_SETOPT($CH, CURLOPT_SSL_VERIFYPEER, TRUE);
        
        $RESPONSE = CURL_EXEC($CH);
        $HTTP_CODE = CURL_GETINFO($CH, CURLINFO_HTTP_CODE);
        CURL_CLOSE($CH);

        IF ($HTTP_CODE == 200) {
            $DATA = JSON_DECODE($RESPONSE, TRUE);
            // EXTRAEMOS EL VALOR DEL CAMPO "CONTRASENA"
            $CLAVE_FIRESTORE = $DATA['FIELDS'][$FIELD_NAME]['STRINGVALUE'] ?? '';

            IF ($CLAVE_INPUT === $CLAVE_FIRESTORE) {
                SESSION_START();
                $_SESSION['EMPRESA'] = $USUARIO_INPUT;
                HEADER("LOCATION: /PANEL-CONTROL.PHP");
                EXIT;
            } ELSE {
                $MENSAJE_ERROR = "LA CONTRASEÑA ES INCORRECTA";
            }
        } ELSEIF ($HTTP_CODE == 404) {
            $MENSAJE_ERROR = "EL USUARIO '" . STRTOUPPER($USUARIO_INPUT) . "' NO FIGURA EN FIRESTORE";
        } ELSE {
            $MENSAJE_ERROR = "ERROR CRÍTICO DE CONEXIÓN (CÓDIGO: $HTTP_CODE)";
        }
    } ELSE {
        $MENSAJE_ERROR = "DEBE RELLENAR TODOS LOS CAMPOS";
    }
}
