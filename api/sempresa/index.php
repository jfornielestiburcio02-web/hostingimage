<?PHP
// CONFIGURACIÓN EXTRAÍDA DE TU CONFIG
$PROJECT_ID = "hostingimage1";
$COLECCION = "usuarios";
$CAMPO_CLAVE = "contrasena";
$MENSAJE_ERROR = "";

IF ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $USUARIO_INPUT = STRTOLOWER(TRIM($_POST['USUARIO'] ?? ''));
    $CLAVE_INPUT = TRIM($_POST['CLAVE'] ?? '');

    IF (!EMPTY($USUARIO_INPUT) && !EMPTY($CLAVE_INPUT)) {
        
        // RUTA DINÁMICA: VAMOS A PROBAR LA RUTA ABSOLUTA
        $URL = "HTTPS://FIRESTORE.GOOGLEAPIS.COM/V1/PROJECTS/$PROJECT_ID/DATABASES/(DEFAULT)/DOCUMENTS/$COLECCION/$USUARIO_INPUT";
        
        $CH = CURL_INIT($URL);
        CURL_SETOPT($CH, CURLOPT_RETURNTRANSFER, TRUE);
        CURL_SETOPT($CH, CURLOPT_TIMEOUT, 10);
        CURL_SETOPT($CH, CURLOPT_SSL_VERIFYPEER, FALSE);
        
        $RESPONSE = CURL_EXEC($CH);
        $HTTP_CODE = CURL_GETINFO($CH, CURLINFO_HTTP_CODE);
        CURL_CLOSE($CH);

        $DATA = JSON_DECODE($RESPONSE, TRUE);

        IF ($HTTP_CODE == 200) {
            $CLAVE_FIRESTORE = $DATA['FIELDS'][$CAMPO_CLAVE]['STRINGVALUE'] ?? '';

            IF ($CLAVE_INPUT === $CLAVE_FIRESTORE) {
                SESSION_START();
                $_SESSION['EMPRESA'] = $USUARIO_INPUT;
                HEADER("LOCATION: /PANEL-CONTROL.PHP");
                EXIT;
            } ELSE {
                $MENSAJE_ERROR = "CONTRASEÑA INCORRECTA.";
            }
        } ELSE {
            // DEBÚG AGRESIVO: SI FALLA, MOSTRAMOS QUÉ VE PHP
            $MSG_API = $DATA['ERROR']['MESSAGE'] ?? "SIN MENSAJE DE ERROR";
            $MENSAJE_ERROR = "ERROR $HTTP_CODE: $MSG_API <BR/>";
            $MENSAJE_ERROR .= "PROYECTO: $PROJECT_ID | USUARIO: $USUARIO_INPUT <BR/>";
            $MENSAJE_ERROR .= "URL CONSULTADA: $URL";
        }
    } ELSE {
        $MENSAJE_ERROR = "RELLENA LOS CAMPOS.";
    }
}
?>
